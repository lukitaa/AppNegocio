    private void form_agregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_form_agregarProductoActionPerformed
        //Obtener los datos asignados en el form.
        String producto = form_producto.getText();
        int stock = Integer.valueOf(form_stock.getText());
        float precio = Float.valueOf(form_precio.getText());
        Proveedores p = obtenerProveedorSeleccionado(form_proveedor);
        boolean error = true;
        //Realizar el alta del producto en la BD
        try {
            ControladoraProductos.agregarProducto(p, producto, stock, precio);
            error = false;
        } catch (InvalidParameterException | StorageException ex) {
            Logger.getLogger(AltaProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Checkear si se quiere seguir agregando mas productos.
        if (!error && JOptionPane.showConfirmDialog(null, "El producto ha sido agregado correctamente. \nDesea seguir agregando productos?", "Agregar m√°s productos", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
            //Vaciar los strings con los datos del producto.
            form_precio.setText(""); form_producto.setText(""); form_stock.setText(""); form_proveedor.setSelectedIndex(-1);
        }
        else
            this.setVisible(false);
    }//GEN-LAST:event_form_agregarProductoActionPerformed

    //Funcion para obtener el proveedor seleccionado de la tabla.
    public Proveedores obtenerProveedorSeleccionado(JComboBox combo){
        Proveedores p = null;
        List<Proveedores> prov = null;
        String provElegido = (String) combo.getSelectedItem();
        if(combo.getSelectedItem() != null){
            try {
                prov = ControladoraProveedores.getProveedores();
                for(Proveedores pr : prov){
                    if(pr.getProveedor().equals(provElegido))
                        p = pr;
                }
            } catch (StorageException ex) {
                Logger.getLogger(VistaProveedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return p;
    }
    
